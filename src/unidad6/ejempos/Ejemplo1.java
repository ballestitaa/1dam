package unidad6.ejempos;

public class Ejemplo1 {

	public static void main(String[] args) {
		
		System.out.printf("Valor '%4.4f' \n", 5123.1473);

		System.out.printf("%3.2f %n %.2f", 5123.1473, 5.25); // %3.2f el float con 2 decimales y 3 enteros, %n salto de linea, %f el siguiente float entero

		
		System.out.printf("valor %s", 25);
	}

}
